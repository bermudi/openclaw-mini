import fs from 'node:fs'

const BT = String.fromCharCode(96)
const content = [
  "import { PrismaClient } from '@prisma/client'",
  "import { createConfiguredPrismaClient } from '@/lib/prisma-client'",
  "",
  "const globalForPrisma = globalThis as unknown as {",
  "  prisma: PrismaClient | undefined",
  "  prismaReady: Promise<void> | undefined",
  "}",
  "",
  "const DEFAULT_PRISMA_LOG: ('error' | 'warn')[] = ['error', 'warn']",
  "",
  "function ensureClient(): { client: PrismaClient; ready: Promise<void> } {",
  "  if (!globalForPrisma.prisma) {",
  "    const configured = createConfiguredPrismaClient({",
  "      log: DEFAULT_PRISMA_LOG,",
  "      scope: 'nextjs',",
  "    })",
  "    globalForPrisma.prisma = configured.client",
  "    globalForPrisma.prismaReady = configured.ready",
  "  }",
  "  return { client: globalForPrisma.prisma, ready: globalForPrisma.prismaReady! }",
  "}",
  "",
  "export const db = new Proxy({} as PrismaClient, {",
  "  get(_target, prop, _receiver) {",
  "    const value = (ensureClient().client as unknown as Record<string | symbol, unknown>)[prop]",
  "    if (typeof value === 'function') {",
  "      return value.bind(ensureClient().client)",
  "    }",
  "    return value",
  "  },",
  "})",
  "",
  "export const dbReady = new Proxy(Promise.resolve() as Promise<void>, {",
  "  get(_target, prop, _receiver) {",
  "    return (ensureClient().ready as unknown as Record<string | symbol, unknown>)[prop]",
  "  },",
  "})",
  "",
  "export async function resetDbClientForTests(): Promise<void> {",
  "  if (globalForPrisma.prisma) {",
  "    await globalForPrisma.prisma." + "$disconnect" + "()",
  "  }",
  "  globalForPrisma.prisma = undefined",
  "  globalForPrisma.prismaReady = undefined",
  "}",
  "",
].join("\n")
fs.writeFileSync("src/lib/db.ts", content)
